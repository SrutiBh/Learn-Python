# This program adds two numbers

num1 = 1.5
num2 = 6.3

# Add two numbers
sum = float(num1) + float(num2)

# Display the sum
print('The sum of {0} and {1} is {2}'.format(num1, num2, sum))

##################################################

#This program subtracts two numbers

sub = float(num1) - float(num2)
#Display the sub
print('The subtraction of {0} and {1} is {2}'.format(num1, num2, sub))

#######################################################

#This program multiplies two numbers

multi = float(num1) * float(num2)
#Display the multi
print('The multiplication of {0} and {1} is {2}'.format(num1, num2, multi))

############################################################

#This program divides two numbers

div = float(num1) / float(num2)
# Display the div
print('The multiplication of {0} and {1} is {2}'.format(num1, num2, div))